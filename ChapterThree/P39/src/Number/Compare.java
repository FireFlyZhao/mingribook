package Number;

public class Compare {

	public static void main(String[] args) {
		int number1 = 4;
		int number2 = 5;
		System.out.println("number1>number2的返回值为:" + (number1 > number2));
		System.out.println("number1<number2的返回值为:" + (number1 < number2));
		System.out.println("number1==number2的返回值为:" + (number1 == number2));
		System.out.println("number1!=number2的返回值为:" + (number1 != number2));
		System.out.println("number1>=number2的返回值为:" + (number1 >= number2));
		System.out.println("number1<=number2的返回值为:" + (number1 <= number2));
	}

}
